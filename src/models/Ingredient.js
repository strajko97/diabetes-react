export class Ingredient {
    constructor(id, name, quantity, kcal, carbohydrates, proteins, fats) {
        this.id = id;
        this.name = name;
        this.quantity = quantity;
        this.kcal = kcal;
        this.carbohydrates = carbohydrates;
        this.proteins = proteins;
        this.fats = fats;
    }
}
