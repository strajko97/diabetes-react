export class Ingredient {
    constructor(name, quantity, kcal, carbohydrates, proteins, fats) {
        this.name = name;
        this.quantity = quantity;
        this.kcal = kcal;
        this.carbohydrates = carbohydrates; // UH in the image
        this.proteins = proteins;           // P in the image
        this.fats = fats;                   // M in the image
    }
}
